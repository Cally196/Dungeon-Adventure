    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        HashMap vychody = this.hrac.getAktualnaMiestnost().getVychody();
        Set<String> zoznam = vychody.keySet();
        String parameter = JOptionPane.showInputDialog(zoznam);

        VykonavacChod chod = new VykonavacChod();

        if (parameter.equals("The Cathedral")) { //kontrola pre kluce do Cathedral
            if (this.hrac.maKluce()) {
                if (this.hrac.vsetkoZabite(parameter)) { //kontroluje ci su vsetky NPC zabite
                    this.chod(chod, parameter);  //prikaz chod
                } else {
                    this.jTextArea1.setText("Najprv musis zabit vsetky prisery");
                }

            } else {
                this.jTextArea1.setText("Najprv najdi kluce Cathedral key 1 \na Cathedral key 2");
            }
        } else {
            if (this.hrac.jeParameter(parameter)) {  //kontrola ci existuje vychod
                if (this.hrac.vsetkoZabite(parameter)) {
                    this.chod(chod, parameter);
                } else {
                    this.jTextArea1.setText("Najprv musis zabit vsetky prisery");
                }
            } else {
                this.jTextArea1.setText("Zadaj spravny vychod");
            }
        }

        this.vypisPredmety();
        this.vypisInventar();
        this.vypisNPC();
        this.vypisAktualnuMiestnost();
    }//GEN-LAST:event_jButton1ActionPerformed


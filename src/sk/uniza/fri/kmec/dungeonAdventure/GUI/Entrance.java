    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        INPC npc = this.hrac.getTarget();

        if (!(npc == null)) {
            Fireball fireball = new Fireball();
            if (!(fireball.utok(this.hrac, npc))) {
                this.jTextArea1.setText("Mas malo many");
            }
            this.jTextField2.setText(this.getHP(npc.getMeno()));
            this.setStats();

            this.isDead();

            if (this.hrac.getTarget().getHP() <= 0) {
                this.hrac.zoberPredmety(this.hrac.getAktualnaMiestnost().getNPC(npc.getMeno()).getPredmety());
                this.hrac.getAktualnaMiestnost().removeNPC(npc.getMeno());
                this.hrac.removeTarget(npc.getMeno());
                this.jTextField1.setText("");
                this.jTextField2.setText("");
                this.vypisPredmety();
                this.vypisInventar();
                this.vypisNPC();
                this.jTextArea1.setText("Zabil si " + npc.getMeno());
            }

        } else {
            this.jTextArea1.setText("Nemas nic v targete");
        }

    }//GEN-LAST:event_jButton7ActionPerformed

